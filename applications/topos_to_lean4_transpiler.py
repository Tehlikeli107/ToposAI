class Lean4Transpiler:
    def __init__(self, entities):
        self.entities = [e.replace(" ", "_").lower() for e in entities]

    def transpile_chain(self, chain, confidence):
        """
        Bulunan bir mantık zincirini (Örn: A -> B -> C) 
        Lean 4 'Theorem' sentaksına dönüştürür.
        """
        if not chain or len(chain) < 2:
            return "Hata: Geçerli bir zincir yok."
            
        lean_code = []
        lean_code.append("-- ==========================================")
        lean_code.append("-- AUTO-GENERATED BY TOPOS-AI TRANSPILER")
        lean_code.append(f"-- Confidence Score: {confidence:.4f}")
        lean_code.append("-- Target Theorem: Formal Proof of Topos Reasoning")
        lean_code.append("-- ==========================================\n")
        
        # 1. Değişken Tanımları (Varlıklar)
        vars_str = " ".join([self.entities[i] for i in chain])
        lean_code.append(f"variable ({vars_str} : Prop)")
        
        # 2. Hipotezler (Zincirin Halka Bağlantıları)
        hypotheses = []
        for idx in range(len(chain) - 1):
            h_name = f"h{idx+1}"
            u = self.entities[chain[idx]]
            v = self.entities[chain[idx+1]]
            lean_code.append(f"variable ({h_name} : {u} → {v})")
            hypotheses.append(h_name)
            
        # 3. Teoremin Kendisi (A -> Sonuç)
        start_node = self.entities[chain[0]]
        end_node = self.entities[chain[-1]]
        theorem_name = f"topos_proof_{start_node}_to_{end_node}"
        
        lean_code.append(f"\n-- ToposAI'nin iddia ettiği nihai sonuç:")
        lean_code.append(f"theorem {theorem_name} : {start_node} → {end_node} := ")
        
        # 4. İspatın (Proof) İnşası (Curry-Howard-Lambek Yazılımı)
        # Fonksiyonel kompozisyon (A -> B ve B -> C ise f(g(x)))
        lean_code.append("  -- Curry-Howard correspondence via functional composition")
        
        # Exact intro
        lean_code.append(f"  intro (x : {start_node})")
        
        # Zinciri geriye doğru sararak fonksiyon çağırma
        proof_call = "x"
        for h in hypotheses:
            proof_call = f"({h} {proof_call})"
            
        lean_code.append(f"  exact {proof_call}")

        return "\n".join(lean_code)

