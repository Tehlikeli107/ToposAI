def monoidal_to_lean4(mc: FiniteMonoidalCategory, name: str = "FinMonCat") -> str:
    """
    Generate a Lean 4 ``MonoidalCategory`` instance from a ``FiniteMonoidalCategory``.

    Builds on ``category_to_lean4`` and adds tensor, associator, and unitor
    definitions together with a ``MonoidalCategory`` instance whose coherence
    axioms are proved by ``decide``.

    Returns
    -------
    str
        A self-contained Lean 4 source file.
    """
    C = mc.category
    objs = C.objects
    obj_ids = {o: _lean_id(o) for o in objs}
    mor_ids = {m: _lean_id(m) for m in C.morphisms}

    # Reuse category export (strip the trailing #check line)
    cat_code = category_to_lean4(C, name=name)
    cat_lines = cat_code.splitlines()
    cat_body = "\n".join(
        ln for ln in cat_lines
        if not ln.startswith(f"#check") and not ln.startswith(f"namespace {name}")
        and not ln.startswith(f"end {name}")
    )

    lines: list[str] = [
        "-- Generated by ToposAI lean4_export.monoidal_to_lean4",
        "-- " + "-" * 68,
        f"-- MonoidalCategory: {name}",
        "-- " + "-" * 68,
        "",
        "import Mathlib.CategoryTheory.Monoidal.Category",
        "import Mathlib.Tactic",
        "",
        f"namespace {name}",
        "",
    ]

    # Embed the category definitions (without the outer namespace wrapper)
    lines += [
        ln for ln in cat_code.splitlines()
        if not ln.startswith("import")
        and not ln.startswith(f"namespace {name}")
        and not ln.startswith(f"end {name}")
        and not ln.startswith("#check")
    ]
    lines.append("")

    # Unit object constant
    lines.append(f"def tensorUnit : Obj := .{obj_ids[mc.unit]}")
    lines.append("")

    # Tensor on objects
    lines.append("def tensorObj (X Y : Obj) : Obj := match X, Y with")
    for (A, B), AB in mc.tensor_objects.items():
        lines.append(f"  | .{obj_ids[A]}, .{obj_ids[B]} => .{obj_ids[AB]}")
    lines.append("")

    # Tensor on morphisms
    lines.append("def tensorHom : {{a b c d : Obj}} → Mor a b → Mor c d → Mor (tensorObj a c) (tensorObj b d)")
    for (f, g), fg in mc.tensor_morphisms.items():
        lines.append(f"  | .{mor_ids[f]}, .{mor_ids[g]} => .{mor_ids[fg]}")
    lines.append("  | f, _ => f  -- unreachable")
    lines.append("")

    # Associator
    lines.append("def assoc (A B C : Obj) : Mor (tensorObj (tensorObj A B) C) (tensorObj A (tensorObj B C)) :=")
    lines.append("  match A, B, C with")
    for (A, B, Cv), a in mc.associator.items():
        lines.append(f"  | .{obj_ids[A]}, .{obj_ids[B]}, .{obj_ids[Cv]} => .{mor_ids[a]}")
    lines.append("")

    # Left unitor
    lines.append("def leftUnit (A : Obj) : Mor (tensorObj tensorUnit A) A := match A with")
    for A, lam in mc.left_unitor.items():
        lines.append(f"  | .{obj_ids[A]} => .{mor_ids[lam]}")
    lines.append("")

    # Right unitor
    lines.append("def rightUnit (A : Obj) : Mor (tensorObj A tensorUnit) A := match A with")
    for A, rho in mc.right_unitor.items():
        lines.append(f"  | .{obj_ids[A]} => .{mor_ids[rho]}")
    lines.append("")

    # MonoidalCategory instance
    lines += [
        "open CategoryTheory MonoidalCategory in",
        "instance : MonoidalCategory Obj where",
        "  tensorObj         := tensorObj",
        "  tensorHom         := fun f g => tensorHom f g",
        "  tensorUnit        := tensorUnit",
        "  associator X Y Z  := ⟨assoc X Y Z, assoc X Y Z, by decide, by decide⟩",
        "  leftUnitor A      := ⟨leftUnit A, leftUnit A, by decide, by decide⟩",
        "  rightUnitor A     := ⟨rightUnit A, rightUnit A, by decide, by decide⟩",
        "  pentagon          := by decide",
        "  triangle          := by decide",
        "",
        f"end {name}",
        "",
        f"#check ({name}.instMonoidalCategoryObj : CategoryTheory.MonoidalCategory {name}.Obj)",
    ]

    return "\n".join(lines)


# ------------------------------------------------------------------ #
# Convenience: export to file                                          #
# ------------------------------------------------------------------ #

def export_to_file(code: str, path: str) -> None:
    """Write generated Lean 4 code to *path*."""
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(code)
