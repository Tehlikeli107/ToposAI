# ------------------------------------------------------------------ #
# Lean identifier sanitisation                                         #
# ------------------------------------------------------------------ #

def _lean_id(s) -> str:
    """Convert an arbitrary Python value to a valid Lean 4 identifier."""
    raw = re.sub(r"[^a-zA-Z0-9_]", "_", str(s))
    raw = raw.strip("_") or "x"
    if raw[0].isdigit():
        raw = "n" + raw
    return raw


def _lean_ids(seq) -> list[str]:
    return [_lean_id(x) for x in seq]


# ------------------------------------------------------------------ #
# FiniteCategory → Lean 4                                              #
# ------------------------------------------------------------------ #

def category_to_lean4(category: FiniteCategory, name: str = "FinCat") -> str:
    """
    Generate a Lean 4 / Mathlib4 ``Category`` instance for a ``FiniteCategory``.

    The output defines:

    * ``{name}Obj`` — an inductive type for objects (``DecidableEq``)
    * ``{name}Mor`` — a dependent inductive type ``{name}Mor : {name}Obj → {name}Obj → Type``
    * ``{name}Comp`` — an explicit composition function built from the composition table
    * A ``CategoryTheory.Category`` instance whose axioms are proved by ``decide``
      (valid because all types are finite and decidably equal)

    Parameters
    ----------
    category : FiniteCategory
    name : str
        Lean namespace / type prefix (must be a valid Lean identifier).

    Returns
    -------
    str
        A self-contained Lean 4 source file.
    """
    objs = category.objects
    mors = category.morphisms      # name → (src, dst)
    ids_map = category.identities  # obj → mor_name
    comp_map = category.composition  # (after, before) → result

    obj_ids = {o: _lean_id(o) for o in objs}
    mor_ids = {m: _lean_id(m) for m in mors}

    lines: list[str] = []

    # Header
    lines += [
        "-- Generated by ToposAI lean4_export.category_to_lean4",
        "-- " + "-" * 68,
        f"-- Category: {name}",
        "-- " + "-" * 68,
        "",
        "import Mathlib.CategoryTheory.Category.Basic",
        "import Mathlib.Tactic",
        "",
        f"namespace {name}",
        "",
    ]

    # Objects inductive
    lines.append(f"inductive Obj : Type where")
    for o in objs:
        lines.append(f"  | {obj_ids[o]} : Obj")
    lines.append("  deriving DecidableEq, Repr, Fintype")
    lines.append("")

    # Morphisms dependent inductive
    lines.append(f"inductive Mor : Obj → Obj → Type where")
    for m, (src, dst) in mors.items():
        lines.append(f"  | {mor_ids[m]} : Mor .{obj_ids[src]} .{obj_ids[dst]}")
    lines.append("  deriving DecidableEq, Repr")
    lines.append("")

    # Identity function
    lines.append("def ident : (X : Obj) → Mor X X")
    for o in objs:
        id_mor = ids_map[o]
        lines.append(f"  | .{obj_ids[o]} => .{mor_ids[id_mor]}")
    lines.append("")

    # Composition function — exhaustive match on composable pairs
    lines.append("def comp : {{a b c : Obj}} → Mor b c → Mor a b → Mor a c")
    for (after, before), result in comp_map.items():
        lines.append(
            f"  | .{mor_ids[after]}, .{mor_ids[before]} => .{mor_ids[result]}"
        )
    # Fallback: Lean requires totality; unreachable cases get a junk value via
    # the identity of the source object (safe because they are never reachable
    # from well-typed terms — Lean's type system prevents composing non-composable morphisms)
    lines.append("  | f, _ => f  -- unreachable: type system prevents non-composable pairs")
    lines.append("")

    # Category instance
    lines += [
        "open CategoryTheory in",
        "instance : Category Obj where",
        "  Hom    := Mor",
        "  id     := ident",
        "  comp   := fun f g => comp f g",
        "  id_comp := by decide",
        "  comp_id := by decide",
        "  assoc   := by decide",
        "",
        f"end {name}",
        "",
        f"#check ({name}.instCategoryObj : CategoryTheory.Category {name}.Obj)",
    ]

    return "\n".join(lines)


# ------------------------------------------------------------------ #
# FiniteFunctor → Lean 4                                              #
# ------------------------------------------------------------------ #

def functor_to_lean4(
    functor: FiniteFunctor,
    source_name: str = "C",
    target_name: str = "D",
    functor_name: str = "F",
) -> str:
    """
    Generate a Lean 4 ``Functor`` definition from a ``FiniteFunctor``.

    Assumes the source and target categories have already been exported
    (e.g. via ``category_to_lean4``) with the given namespace names.

    Returns
    -------
    str
        Lean 4 snippet defining ``{functor_name} : {source_name}.Obj ⥤ {target_name}.Obj``.
    """
    C = functor.source
    obj_map = functor.object_map    # src_obj → tgt_obj
    mor_map = functor.morphism_map  # src_mor → tgt_mor

    src_obj_ids = {o: _lean_id(o) for o in C.objects}
    tgt_obj_ids = {o: _lean_id(o) for o in functor.target.objects}
    src_mor_ids = {m: _lean_id(m) for m in C.morphisms}
    tgt_mor_ids = {m: _lean_id(m) for m in functor.target.morphisms}

    lines: list[str] = [
        "-- Generated by ToposAI lean4_export.functor_to_lean4",
        f"-- Functor {functor_name} : {source_name} ⥤ {target_name}",
        "",
        "open CategoryTheory in",
        f"def {functor_name} : {source_name}.Obj ⥤ {target_name}.Obj where",
        "  obj x := match x with",
    ]
    for src_o, tgt_o in obj_map.items():
        lines.append(f"    | .{src_obj_ids[src_o]} => .{tgt_obj_ids[tgt_o]}")
    lines.append("  map f := match f with")
    for src_m, tgt_m in mor_map.items():
        lines.append(f"    | .{src_mor_ids[src_m]} => .{tgt_mor_ids[tgt_m]}")
    lines += [
        "  map_id  := by decide",
        "  map_comp := by decide",
        "",
    ]
    return "\n".join(lines)


# ------------------------------------------------------------------ #
# NaturalTransformation → Lean 4                                      #
# ------------------------------------------------------------------ #

def nat_trans_to_lean4(
    transformation: NaturalTransformation,
    source_functor_name: str = "F",
    target_functor_name: str = "G",
    nat_name: str = "alpha",
    category_name: str = "C",
) -> str:
    """
    Generate a Lean 4 ``NatTrans`` from a ``NaturalTransformation``.

    The presheaves are viewed as functors ``C^op ⥤ Type``; the components
    are stated as explicit match arms and naturality is proved by ``decide``.

    Returns
    -------
    str
        Lean 4 snippet defining ``{nat_name} : {source_functor_name} ⟶ {target_functor_name}``.
    """
    src = transformation.source
    C = src.category
    obj_ids = {o: _lean_id(o) for o in C.objects}

    lines: list[str] = [
        "-- Generated by ToposAI lean4_export.nat_trans_to_lean4",
        f"-- NatTrans {nat_name} : {source_functor_name} ⟶ {target_functor_name}",
        "",
        "open CategoryTheory in",
        f"def {nat_name} : {source_functor_name} ⟶ {target_functor_name} where",
        "  app X := match X with",
    ]
    for obj in C.objects:
        comp = transformation.components[obj]
        # Represent the component as a function on the (finite) source set
        comp_arms = "  | " + " | ".join(
            f".{_lean_id(k)} => .{_lean_id(v)}" for k, v in comp.items()
        ) if comp else "  | _ => sorry"
        lines.append(f"    | .{obj_ids[obj]} => fun x => match x with")
        for k, v in comp.items():
            lines.append(f"      | .{_lean_id(k)} => .{_lean_id(v)}")
    lines += [
        "  naturality := by decide",
        "",
    ]
    return "\n".join(lines)


