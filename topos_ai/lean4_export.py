"""
Lean 4 / Mathlib4 code generation from ToposAI formal structures.

Each exporter function returns a self-contained string of Lean 4 source code
that imports Mathlib and defines the relevant structure.  The generated proofs
use ``decide`` — which works for any finite ``DecidableEq`` type — so every
axiom is machine-checked, not merely stated.
"""

from __future__ import annotations

